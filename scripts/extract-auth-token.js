#!/usr/bin/env node
/**
 * Script pour extraire le token d'authentification depuis les cookies du navigateur
 * et le sauvegarder pour les tests d'agents
 *
 * Usage:
 *   1. Connecte-toi sur http://127.0.0.1:4174
 *   2. Ouvre la console du navigateur
 *   3. Copie/colle ce code dans la console:
 *
 *      ```javascript
 *      function getCookie(name) {
 *        const value = `; ${document.cookie}`;
 *        const parts = value.split(`; ${name}=`);
 *        if (parts.length === 2) return parts.pop().split(';').shift();
 *      }
 *      const token = getCookie('sojori_token');
 *      const refreshToken = getCookie('sojori_refresh_token');
 *      console.log('SOJORI_API_TOKEN=' + token);
 *      console.log('SOJORI_REFRESH_TOKEN=' + refreshToken);
 *      ```
 *
 *   4. Copie le token affiché
 *   5. Lance ce script: node scripts/extract-auth-token.js <TOKEN>
 */

const fs = require('fs');
const path = require('path');

const token = process.argv[2];

if (!token) {
  console.error('❌ Usage: node scripts/extract-auth-token.js <TOKEN>');
  console.error('');
  console.error('📋 Pour obtenir le token:');
  console.error('   1. Connecte-toi sur http://127.0.0.1:4174');
  console.error('   2. Ouvre la console du navigateur (F12)');
  console.error('   3. Copie/colle ce code:');
  console.error('');
  console.error('      function getCookie(name) {');
  console.error('        const value = `; ${document.cookie}`;');
  console.error('        const parts = value.split(`; ${name}=`);');
  console.error('        if (parts.length === 2) return parts.pop().split(\';\').shift();');
  console.error('      }');
  console.error('      const token = getCookie(\'sojori_token\');');
  console.error('      console.log(\'Token:\', token);');
  console.error('');
  process.exit(1);
}

const envTestPath = path.join(__dirname, '..', '.env.test');

const envContent = `# Auto-generated by extract-auth-token.js
# Token extrait le ${new Date().toISOString()}
SOJORI_API_TOKEN=${token}

# Pour tester les APIs:
# export $(cat .env.test | xargs)
# curl -H "Authorization: Bearer $SOJORI_API_TOKEN" https://dev.sojori.com/api/v1/user/auth/me
`;

fs.writeFileSync(envTestPath, envContent);

console.log('✅ Token sauvegardé dans .env.test');
console.log('');
console.log('📋 Pour utiliser le token dans tes tests:');
console.log('');
console.log('   export $(cat .env.test | xargs)');
console.log('   echo $SOJORI_API_TOKEN');
console.log('');
console.log('🧪 Tester le token:');
console.log('');
console.log('   curl -H "Authorization: Bearer $SOJORI_API_TOKEN" https://dev.sojori.com/api/v1/user/auth/me');
console.log('');
